package Operators;
public class EqualNotEqualOperators {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;

        // Equal Operator
        System.out.println("Equal: " + (num1 == num2));

        // Not Equal Operator
        System.out.println("Not Equal: " + (num1 != num2));
    }
}
